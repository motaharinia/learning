package com.motaharinia.javacore.tjava9feachers.kcontrolpanel;

/**
 * https://www.javatpoint.com/java-9-control-panel
 * Java 9 Control Panel:
 * Java control panel is used to control Java applications that are embedded in browser.
 * This control panel maintains the settings that manage Java application embedded in browser.
 * In Java 9, control panel was rewritten as a JavaFX application and the storage location has changed.
 * To start control panel, enter jcontrol to the terminal (cmd for windows) and it will open a window that looks below.
 * It contains the following tabs
 * کنترل پنل Java 9:
 * کنترل پنل جاوا برای کنترل برنامه های جاوا که در مرورگر تعبیه شده اند ، استفاده می شود.
 * این کنترل پنل تنظیمات مدیریت برنامه جاوا را که در مرورگر تعبیه شده است ، حفظ می کند.
 * در جاوا 9 ، صفحه کنترل به عنوان یک برنامه JavaFX بازنویسی شد و مکان ذخیره سازی تغییر کرده است.
 * برای شروع کنترل پنل ، jcontrol را به ترمینال (cmd برای ویندوز) وارد کنید و پنجره ای باز می شود که در زیر مشاهده می شود.
 * این برگه های زیر را شامل می شود
 *
 * General:
 * This tab shows version details and security status of JRE Java runtime (JRE) that we are running.
 * General:
 * این برگه جزئیات نسخه و وضعیت امنیتی JRE جاوا (JRE) زمان اجرا را نشان می دهد.
 *
 * Update:
 * This tab shows when the check for updates is done and enables us to change the settings for the update process.
 * Update:
 * این برگه نشان می دهد که بررسی برای به روز رسانی ها انجام شده است و ما را قادر می سازد تا تنظیمات مربوط به روند بروزرسانی را تغییر دهیم.
 *
 * Desktop Settings:
 * This tab shows information about the JREs that are installed on our system and enables us to choose the JREs that we want to use to run applications that are embedded in a web page or launched from a browser.
 * Desktop Settings:
 * این برگه اطلاعات مربوط به JRE هایی را که در سیستم ما نصب شده اند نشان می دهد و ما را قادر می سازد تا JRE هایی را که می خواهیم از آنها استفاده کنیم برای اجرای برنامه هایی که در یک صفحه وب تعبیه شده اند یا از یک مرورگر راه اندازی شده است ، انتخاب کنیم.
 *
 * Web Settings:
 * This tab shows information about permissions for Java applications and how the applications connect to the network. It also allows us to manage temporary files.
 * The Web Settings tab further contains the following tabs.
 * 1. Exception Site List Tab
 * 2. Deployment Rule Set Tab
 * 3. Temporary Files Settings Tab
 * 4. Network Settings Tab
 * 5. Java Cache Viewer Tab
 * Web Settings:
 * این برگه اطلاعات مربوط به مجوزهای برنامه های جاوا و نحوه اتصال برنامه ها به شبکه را نشان می دهد. همچنین به ما امکان می دهد فایل های موقتی را مدیریت کنیم.
 * برگه تنظیمات وب برگه های زیر را در بر می گیرد.
 * 1. برگه لیست استثناء
 * 2. برگه تنظیم قانون استقرار
 * 3. زبانه تنظیمات فایل های موقت
 * 4. برگه تنظیمات شبکه
 * 5. برگه نمایشگر حافظه نهان جاوا
 *
 * Security:
 * It shows general security settings and information about certificates used to sign RIAs.
 * The Security tab further contains the following tabs.
 * 1. General Security Settings Tab
 * 2. Manage Certificates Tab
 * Security:
 * تنظیمات امنیتی عمومی و اطلاعات مربوط به گواهینامه های استفاده شده برای امضای RIA را نشان می دهد.
 * برگه امنیت در برگیرنده برگه های زیر است.
 * 1. برگه تنظیمات امنیتی عمومی
 * 2. برگه گواهینامه ها را مدیریت کنید
 *
 * Advanced:
 * It allows us to set options for available JRE. JRE has following options.
 * 1. Debugging
 * 2. Java Console
 * 3. Shortcut Creation
 * 4. JNLP File/MIME Association
 * 5. Application Installation
 * 6. Execution Environment Security Settings
 * 7. Mixed code (sandboxed vs. trusted) security verification
 * 8. Perform signed code certificate revocation checks on
 * 9. Check for signed code certificate revocation using
 * 10. Advanced Security Settings
 * 11. Miscellaneous
 * Advanced:
 * به ما امکان می دهد گزینه هایی را برای JRE موجود تنظیم کنیم. JRE گزینه های زیر را دارد.
 * 1. اشکال زدایی
 * 2. کنسول جاوا
 * 3. ایجاد میانبر
 * 4. JNLP File/MIME Association
 * 5. نصب برنامه
 * 6. تنظیمات امنیتی محیط اجرا
 * 7. تأیید امنیت کد مخلوط (sandboxed vs. trusted)
 * 8- بررسی های ابطال گواهی کد امضا شده را انجام دهید
 * 9. با استفاده از بررسی مجوز گواهی کد امضا شده را بررسی کنید
 * 10. تنظیمات پیشرفته امنیتی
 * 11. متفرقه
 */
